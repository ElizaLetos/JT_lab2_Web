public class Word {
    private String letters;
    private int size;

    public String getLetters() {
        return letters;
    }
    public void setLetters(String letters) {
        this.letters = letters;
    }
    public int getSize() {
        return size;
    }
    public void setSize(int size) {
        this.size = size;
    }
}

